#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int	i1;
	int	i2;
	int	i20;

	i1 = ft_strlen(s1);
	i2 = ft_strlen(s2);
	i20 = 0;
	while (i20 != i2)
	{
		s1[i1 + i20] = s2[i20];
		i20++;
	}
	s1[i1 + i20] = '\0';
	return (s1);
}
