#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	int				t;
	int				i;
	unsigned char	*str1;
	unsigned char	*str2;

	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	i = 0;
	t = 0;
	while (t == 0 && n-- != 0)
	{
		t = str1[i] - str2[i];
		i++;
	}
	return (t);
}
